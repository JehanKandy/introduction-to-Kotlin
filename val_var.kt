//introduction to Kotlin Programming

fun main(){
    //this is a comment
    
    //val --> read-only    
    val fn = "Jehan \n"
    //\n mean leave a line
    print(fn)

    //var --> read and write
    var number = 10
    print("$number \n")

    //Double
    val jkdouble: Double = 2.5
    print(jkdouble)

    //Boolean
    val jkboolt:Boolean = true
    val jkboolf:Boolean = false

    print("\n $jkboolt, $jkboolf")
    


}